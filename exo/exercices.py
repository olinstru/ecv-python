# Exercice 03 : Save the date
# Stocker dans trois variables le jour, le mois et l'année de la date suivante :

date = ['0', '1', '/', '0', '1', '/', '1', '9', '7', '0']

jour = date[:2]
mois = date[3:5]
annee = date[6:]

print(jour, mois, annee, sep="/")

# sep="" est un sépareteur, on définit ce que l'on veut dans nos guillemets, par exemple un "/"" entre les chiffres des dates.

